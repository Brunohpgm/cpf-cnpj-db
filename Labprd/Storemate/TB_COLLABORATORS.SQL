ALTER TABLE STOREMATE.TB_COLLABORATORS ADD DOCUMENT_NEW VARCHAR2(14);

UPDATE STOREMATE.TB_COLLABORATORS
   SET DOCUMENT_NEW = CASE
                        WHEN LENGTH(DOCUMENT) <= 10 THEN
                         LPAD(TO_CHAR(DOCUMENT), 11, '0')
                        ELSE
                         TO_CHAR(DOCUMENT)
                      END;

