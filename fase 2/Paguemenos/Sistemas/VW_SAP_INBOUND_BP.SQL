    ON A.ID_PESSOA = P.ID_PESSOA
 INNER JOIN SISTEMAS.TB_CIDADE C
    ON C.ID_CIDADE = P.ID_CIDADE
 INNER JOIN SISTEMAS.TB_UF U
    ON U.ID_UF = P.ID_UF
