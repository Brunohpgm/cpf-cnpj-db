--validar@@@@@@@@@@@@@@@-- saida para cpf que estão nulos, para poder recriar uq cpf
DECLARE
  V_CPF INTEGER := 1;
BEGIN
  FOR X IN (SELECT ID_PESSOA FROM SISTEMAS.TB_PESSOA WHERE CPF IS NULL) LOOP
    UPDATE SISTEMAS.TB_PESSOA A
       SET A.CPF = TO_CHAR(V_CPF)
     WHERE A.ID_PESSOA = X.ID_PESSOA;
  
    COMMIT;
  
    V_CPF := V_CPF + 1;
  END LOOP;
END;

