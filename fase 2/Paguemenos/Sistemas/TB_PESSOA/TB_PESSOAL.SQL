ALTER TABLE SISTEMAS.TB_PESSOA ADD CPF_2_TEMP VARCHAR2(14); --USADA PARA TRANSIÇÃO

UPDATE
    SISTEMAS.TB_PESSOA A
   SET A.CPF_2_TEMP = CASE
                        WHEN A.TIPO_PESSOA = 'F' THEN
                         LPAD(TO_CHAR(A.CPF), 11, '0')
                        ELSE
                         LPAD(TO_CHAR(A.CPF), 14, '0')
                      END;

UPDATE SISTEMAS.TB_PESSOA A SET A.CPF_2 = TO_CHAR(A.CPF);

