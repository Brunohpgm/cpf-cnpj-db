UPDATE SISTEMAS.TB_PESSOA A
   SET A.CPF_2_TEMP = CASE
                        WHEN A.TIPO_PESSOA = 'F' AND LENGTH(A.CPF) <= 11 THEN
                         LPAD(TO_CHAR(A.CPF), 11, '0')
                        WHEN A.TIPO_PESSOA = 'F' AND LENGTH(A.CPF) > 11 THEN
                         TO_CHAR(A.CPF)
                        ELSE
                         LPAD(TO_CHAR(A.CPF), 14, '0')
                      END;

