ALTER TABLE ATIVMOB.TB_PEDIDO ADD CNPJ_CHAR VARCHAR2(14);

UPDATE ATIVMOB.TB_PEDIDO
   SET CNPJ_CHAR = CASE
                     WHEN LENGTH(CNPJ) < 14 THEN
                      LPAD(TO_CHAR(CNPJ), 14, '0')
                     ELSE
                      TO_CHAR(CNPJ)
                   END;

    ALTER TABLE ATIVMOB.TB_PEDIDO DROP COLUMN CNPJ;               

    ALTER TABLE ATIVMOB.TB_PEDIDO RENAME COLUMN CNPJ_CHAR TO CNPJ;