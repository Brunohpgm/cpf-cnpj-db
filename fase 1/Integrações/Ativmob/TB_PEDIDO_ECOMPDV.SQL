ALTER TABLE ATIVMOB.TB_PEDIDO_ECOMPDV ADD CNPJ_CHAR VARCHAR2(14);


UPDATE ATIVMOB.TB_PEDIDO_ECOMPDV
   SET CNPJ_CHAR = CASE
                     WHEN LENGTH(CNPJ) < 14 THEN
                      LPAD(TO_CHAR(CNPJ), 14, '0')
                     ELSE
                      TO_CHAR(CNPJ)
                   END;                      
                   
ALTER TABLE ATIVMOB.TB_PEDIDO_ECOMPDV DROP COLUMN CNPJ;   

ALTER TABLE ATIVMOB.TB_PEDIDO_ECOMPDV RENAME COLUMN CNPJ_CHAR TO CNPJ;
